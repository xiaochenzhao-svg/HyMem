ACCURACY_PROMPT = """
You are an answer scoring expert.
You will receive the following information: (1) a question, (2) a standard (reference answer), (3) an answer generated by a memory-based LLM. Your task is to label the generated answer as CORRECT or WRONG.
Important notes:
Be as lenient as possible when scoring: the standard answer is typically a concise short sentence, while the generated answer may be longer and more detailed. As long as the generated answer aligns with the standard answer, it should be marked as CORRECT. Do not be overly nitpicky.
For time-related questions, the standard answer is a specific date, month, or year. The generated answer may be longer or use relative time expressions (e.g., "last Tuesday" or "next month"). Please be lenient in scoring—if the generated answer refers to the same time period as the standard answer, mark it as CORRECT.
Finally, provide CORRECT or WRONG.
Do not include both CORRECT and WRONG in your response, as this will cause errors in the evaluation script.
Simply return the label in JSON format with the key "label".
Now it's time for the formal question:
Question: {question}
Gold answer: {gold_answer}
Generated answer: {generated_answer}
"""

def simple_tokenize(text):
    """Simple tokenization function."""
    # Convert to string if not already
    text = str(text)
    return text.lower().replace(".", " ").replace(",", " ").replace("!", " ").replace("?", " ").split()


def evaluate_llm_judge(question, gold_answer, generated_answer):
    """Evaluate the generated answer against the gold answer using an LLM judge."""
    response = client.chat.completions.create(
        model="gpt-4.1-mini",
        messages=[
            {
                "role": "user",
                "content": ACCURACY_PROMPT.format(
                    question=question, gold_answer=gold_answer, generated_answer=generated_answer
                ),
            }
        ],
        # response_format={"type": "json_object"},
        temperature=1.0,
    )
    content = response.choices[0].message.content
    try:
        label = json.loads(content)["label"]
    except Exception:
        # 保底逻辑：用正则匹配是否有CORRECT
        if re.search(r"\bCORRECT\b", content, flags=re.IGNORECASE):
            label = "CORRECT"
        else:
            label = "WRONG"
    return 1 if label == "CORRECT" else 0


def calculate_metrics(prediction: str, reference: str) -> Dict[str, float]:
    """Calculate comprehensive evaluation metrics for a prediction."""
    # Handle empty or None values
    if not prediction or not reference:
        return {
            "exact_match": 0,
            "f1": 0.0,
            "rouge1_f": 0.0,
            "rouge2_f": 0.0,
            "rougeL_f": 0.0,
            "bleu1": 0.0,
            "bleu2": 0.0,
            "bleu3": 0.0,
            "bleu4": 0.0,
            "bert_f1": 0.0,
            "meteor": 0.0,
            "sbert_similarity": 0.0,
        }

    # Convert to strings if they're not already
    prediction = str(prediction).strip()
    reference = str(reference).strip()

    # Calculate exact match
    exact_match = int(prediction.lower() == reference.lower())

    # Calculate token-based F1 score
    pred_tokens = set(simple_tokenize(prediction))
    ref_tokens = set(simple_tokenize(reference))
    common_tokens = pred_tokens & ref_tokens

    if not pred_tokens or not ref_tokens:
        f1 = 0.0
    else:
        precision = len(common_tokens) / len(pred_tokens)
        recall = len(common_tokens) / len(ref_tokens)
        f1 = 2 * precision * recall / (precision + recall) if (precision + recall) > 0 else 0.0

    # Calculate all scores
    bleu_scores = calculate_bleu_scores(prediction, reference)

    # Combine all metrics
    metrics = {
        "exact_match": exact_match,
        "f1": f1,
        **bleu_scores,
    }

    return metrics


def calculate_bleu_scores(prediction: str, reference: str) -> Dict[str, float]:
    """Calculate BLEU scores with different n-gram settings."""
    pred_tokens = nltk.word_tokenize(prediction.lower())
    ref_tokens = [nltk.word_tokenize(reference.lower())]

    weights_list = [(1, 0, 0, 0), (0.5, 0.5, 0, 0), (0.33, 0.33, 0.33, 0), (0.25, 0.25, 0.25, 0.25)]
    smooth = SmoothingFunction().method1

    scores = {}
    for n, weights in enumerate(weights_list, start=1):
        try:
            score = sentence_bleu(ref_tokens, pred_tokens, weights=weights, smoothing_function=smooth)
        except Exception as e:
            print(f"Error calculating BLEU score: {e}")
            score = 0.0
        scores[f"bleu{n}"] = score

    return scores


def process_item(item):
    """Process a single item from the results"""
    gt_answer = str(item["answer"])
    pred_answer = str(item["response"])
    question = str(item["question"])

    # Get category if it exists in metadata
    category = str(item["category"])

    try:
        metrics = calculate_metrics(pred_answer, gt_answer)
        bleu_scores = calculate_bleu_scores(pred_answer, gt_answer)
        llm_score = evaluate_llm_judge(question, gt_answer, pred_answer)
        # llm_score = 0
        result = {
            "question": question,
            "answer": gt_answer,
            "response": pred_answer,
            "bleu_score": bleu_scores.get("bleu1", 0),
            "f1_score": metrics.get("f1", 0),
            "llm_score": llm_score,
        }

        # Add category only if it exists
        if category is not None:
            result["category"] = category

        return result
    except Exception as e:
        print(f"Error processing item: {e}")
        print(f"Question: {question}")
        print(f"GT Answer: {gt_answer}")
        print(f"Predicted Answer: {pred_answer}")
        # Return default values if processing fails
        result = {
            "question": question,
            "answer": gt_answer,
            "response": pred_answer,
            "bleu_score": 0.0,
            "f1_score": 0.0,
            "llm_score": 0.0,
        }
        if category is not None:
            result["category"] = category
        return result


def calculate_category_stats(results):
    """Calculate statistics grouped by category if categories exist"""
    category_stats = {}

    # Check if any results have categories
    has_categories = any("category" in item for item in results)
    if not has_categories:
        return None

    # Define the five categories
    categories = ['1', '2', '3', '4', '5']

    # Group results by category
    category_results = defaultdict(list)
    for item in results:
        if "category" in item:
            category_results[item["category"]].append(item)

    # Calculate stats for each category
    for category in categories:
        items = category_results.get(category, [])
        if items:
            bleu_scores = [item["bleu_score"] for item in items]
            f1_scores = [item["f1_score"] for item in items]
            llm_scores = [item["llm_score"] for item in items]

            category_stats[category] = {
                "num_questions": len(items),
                "avg_bleu_score": sum(bleu_scores) / len(bleu_scores) if bleu_scores else 0,
                "avg_f1_score": sum(f1_scores) / len(f1_scores) if f1_scores else 0,
                "avg_llm_score": sum(llm_scores) / len(llm_scores) if llm_scores else 0,
            }
        else:
            category_stats[category] = {
                "num_questions": 0,
                "avg_bleu_score": 0,
                "avg_f1_score": 0,
                "avg_llm_score": 0,
            }

    return category_stats


def extract_field(log_line, field_name):
    # 匹配 "Prediction:"，"Reference:"，"Category:" 等字段，不管前面有多少日志前缀
    pattern = re.compile(rf"{field_name}:\s*(.*)")
    found = pattern.search(log_line)
    if found:
        return found.group(1).strip()
    return None


def parse_log_file(filename):
    data = {}
    with open(filename, "r", encoding="utf-8") as f:
        lines = f.readlines()

    def split_field(line):
        m = re.match(r".*?\b(Prediction|Reference|Category|User Prompt):\s*(.*)", line)
        if m:
            return m.group(1), m.group(2)
        return None, None

    i = 0
    while i < len(lines):
        line = lines[i].strip()
        m_q = re.match(r".*Question (\d+): (.+)", line)
        if not m_q:
            i += 1
            continue

        qnum = m_q.group(1)
        question_text = m_q.group(2).strip()
        curr_qid = f"Question {qnum}"

        curr_item = {
            "question": question_text,
            "response": "",
            "answer": "",
            "category": None,
        }

        i += 1
        current_field = None
        buffer = []

        def flush_buffer():
            nonlocal buffer, current_field
            if not current_field:
                return
            text = "\n".join(buffer).rstrip()

            if current_field == "Prediction":
                if curr_item["response"] == "":
                    curr_item["response"] = text
            elif current_field == "Reference":
                if curr_item["answer"] == "":
                    curr_item["answer"] = text
            elif current_field == "Category":

                if curr_item["category"] is None and buffer:
                    # 只保留第一行内容
                    curr_item["category"] = buffer[0].strip()

            buffer = []
            current_field = None

        while i < len(lines):
            nxt = lines[i].rstrip("\n")
            stripped = nxt.strip()

            if re.match(r".*Question \d+: ", stripped):
                flush_buffer()
                break

            field_name, content = split_field(nxt)

            if field_name:
                flush_buffer()
                current_field = field_name
                if content:
                    buffer.append(content)
            else:
                # 对 Prediction/Reference/User Prompt 允许多行
                if current_field in ("Prediction", "Reference", "User Prompt"):
                    buffer.append(nxt.strip())

            i += 1

        flush_buffer()
        data[curr_qid] = [curr_item]

    return data


def main():
    parser = argparse.ArgumentParser(description="Evaluate memory agent results")
    parser.add_argument(
        "--log_file", type=str, default="",
        help="Path to the input results file or directory from eval.py"
    )
    parser.add_argument(
        "--temp_file", type=str,
        default="",
        help="Path to the input results file or directory from eval.py"
    )
    parser.add_argument(
        "--output_file", type=str,
        default="",
        help="Path to save the evaluation results"
    )
    parser.add_argument("--max_workers", type=int, default=1, help="Maximum number of worker threads")

    args = parser.parse_args()

    all_results = []
    results_lock = threading.Lock()
    all_items = []

    data = parse_log_file(args.log_file)
    with open(args.temp_file, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=4, ensure_ascii=False)

    with open(args.temp_file, "r", encoding="utf-8") as f:
        data = json.load(f)

    for conversation_id, items in data.items():
        for item in items:
            all_items.append((conversation_id, item))

    print(f"Processing {len(all_items)} items across {len(data)} conversations...")

    # Use ThreadPoolExecutor with specified workers
    with concurrent.futures.ThreadPoolExecutor(max_workers=args.max_workers) as executor:
        future_to_item = {
            executor.submit(process_item, item): (conversation_id, item)
            for conversation_id, item in all_items
        }

        for future in tqdm(concurrent.futures.as_completed(future_to_item), total=len(future_to_item)):
            conversation_id, original_item = future_to_item[future]
            processed_result = future.result()
            with results_lock:
                all_results.append(processed_result)

    # Calculate overall statistics
    all_bleu_scores = [item["bleu_score"] for item in all_results]
    all_f1_scores = [item["f1_score"] for item in all_results]
    all_llm_scores = [item["llm_score"] for item in all_results]

    # Overall statistics
    overall_stats = {
        "total_questions": len(all_results),
        "overall_avg_bleu_score": sum(all_bleu_scores) / len(all_bleu_scores) if all_bleu_scores else 0,
        "overall_avg_f1_score": sum(all_f1_scores) / len(all_f1_scores) if all_f1_scores else 0,
        "overall_avg_llm_score": sum(all_llm_scores) / len(all_llm_scores) if all_llm_scores else 0,
    }

    # Calculate category stats if categories exist
    category_stats = calculate_category_stats(all_results)

    # Prepare final output
    final_results = {
        "detailed_results": all_results,
        "overall_summary": overall_stats
    }

    # Add category stats only if they exist
    if category_stats is not None:
        final_results["summary_by_category"] = category_stats

    # Save results to JSON file
    with open(args.output_file, "w", encoding="utf-8") as f:
        json.dump(final_results, f, indent=4, ensure_ascii=False)

    print(f"Results saved to {args.output_file}")
    print("\n=== Evaluation Summary ===")
    print(f"Total Questions: {overall_stats['total_questions']}")
    print(f"Overall Average BLEU Score: {overall_stats['overall_avg_bleu_score']:.4f}")
    print(f"Overall Average F1 Score: {overall_stats['overall_avg_f1_score']:.4f}")
    print(f"Overall Average LLM Score: {overall_stats['overall_avg_llm_score']:.4f}")

    # Print category-wise statistics if they exist
    if category_stats is not None:
        CATEGORY_LABEL_MAP = {
            "1": "Single Hop",
            "2": "Multi-Hop",
            "3": "Open Domain",
            "4": "Temporal",
            "5": "Adversarial"
        }

        print("\n=== Category-wise Statistics ===")
        for num_id, category_name in CATEGORY_LABEL_MAP.items():
            stats = category_stats.get(num_id, {
                "num_questions": 0,
                "avg_bleu_score": 0.0,
                "avg_f1_score": 0.0,
                "avg_llm_score": 0.0
            })
            print(f"\n{category_name}:")
            print(f"  Questions: {stats['num_questions']}")
            print(f"  Average BLEU Score: {stats['avg_bleu_score']:.4f}")
            print(f"  Average F1 Score: {stats['avg_f1_score']:.4f}")
            print(f"  Average LLM Score: {stats['avg_llm_score']:.4f}")


if __name__ == "__main__":
    main()
